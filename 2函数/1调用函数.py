# 一些python内置的函数

# abs() 求绝对值
print(abs(-100))


# max() min() 求最大值最小值  mind
print(max(1,2,3,-1))
a= min(1,2,3,4,-1)
print(a)

#hex() 转化成十六进制的字符串表示
a1= hex(10)
print("hex() =  "+a1)
print(type(a1))

# 内置的数据类型转换函数

# int() 转为Int
i= int('123')
print(i)

b=int(12.34)
print(b)


# float() 转为float
float('12.34')

# str()转化为字符串
str(1.23)
str(100)

# bool() 转化为bool
bool(1)

bool('')




